"""
You are given an m x n integer matrix grid where each cell is either 0 (empty)
or 1 (obstacle). You can move up, down, left, or right from and to an empty
cell in one step.

Return the minimum number of steps to walk from the upper left corner (0, 0)
to the lower right corner (m - 1, n - 1) given that you can eliminate at most
k obstacles. If it is not possible to find such walk return -1.

Constraints:
m == grid.length
n == grid[i].length
1 <= m, n <= 40
1 <= k <= m * n
grid[i][j] is either 0 or 1.
grid[0][0] == grid[m - 1][n - 1] == 0
"""


from collections import deque
import unittest


class Solution:
    def shortestPath(self, grid: list[list[int]], k: int) -> int:
        num_rows = len(grid)
        num_cols = len(grid[0])

        if k > num_rows + num_cols - 3:
            return num_rows + num_cols - 2

        target = (num_rows - 1, num_cols - 1)
        state = (0, 0, k)

        visited: set[tuple[int, int, int]] = set()
        visited.add(state)

        queue: deque[tuple[int, tuple[int, int, int]]] = deque()
        queue.append((0, state))

        while queue:
            steps, (row, col, remaining) = queue.popleft()

            if (row, col) == target:
                return steps

            for row, col in [(row + 1, col), (row, col + 1),
                             (row - 1, col), (row, col - 1)]:
                if (0 <= row < num_rows) and (0 <= col < num_cols):
                    remaining -= grid[row][col]
                    state = (row, col, remaining)

                    if remaining >= 0 and state not in visited:
                        queue.append((steps + 1, state))
                        visited.add(state)

        return -1


